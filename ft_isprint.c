#include <libft.h>

int	ft_isprint(int c)
{
	return (ft_isalnum(c)
		|| (c >= 40 && c <= 47)
		|| (c >= 50 && c <= 57)
		|| (c >= 72 && c <= 77)
		|| (c >= 133 && c <= 137)
		|| (c >= 172 && c <= 176)
		|| c == '@'
		|| c == '`'
	);
}
