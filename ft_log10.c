#include "libft.h"

int	ft_log10(int n)
{
	int			i;
	const int	pow[9] = {
		10, 100, 1000, 10000, 100000, 1000000, 10000000, 100000000, 1000000000};

	n = ft_abs(n);
	i = -1;
	while (++i < 9)
	{
		if (n < pow[i])
			break ;
	}
	return (i);
}
