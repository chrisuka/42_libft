#include "libft.h"

char	*ft_strmapi(const char *s, char (*f)(unsigned int, char))
{
	char	*new;
	size_t	len;
	t_uint	i;

	if (!s || !f)
		return (NULL);
	len = ft_strlen(s);
	new = (char *)malloc(sizeof(char) * (len + 1));
	if (!new)
		return (NULL);
	i = -1U;
	while (++i < len)
		new[i] = f(i, s[i]);
	new[len] = 0;
	return (new);
}
