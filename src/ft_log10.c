#include "libft.h"

int	ft_log10(int n)
{
	t_uint			nu;
	int				i;
	const t_uint	pow[9] = {
		10, 100, 1000, 10000, 100000, 1000000, 10000000, 100000000, 1000000000};

	nu = ft_abs(n);
	i = -1;
	while (++i < 9)
	{
		if (nu < pow[i])
			break ;
	}
	return (i);
}
