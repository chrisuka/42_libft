#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*p1;
	char	*p2;

	p1 = (char *)s1;
	p2 = (char *)s2;
	while (*p1 != '\0')
		p1++;
	while (n-- > 0 && *p2 != '\0')
		*p1++ = *p2++;
	*p1 = '\0';
	return (s1);
}
